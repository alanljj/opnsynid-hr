from openerp import models, fields


class HrJobFamily(models.Model):
    _name = "hr.job_family"
    _description = "Job Family"

    name = fields.Char(
        string="Job Family",
        required=True,
        translate=True,
    )
    code = fields.Char(
        string="Code",
        required=True,
    )
    active = fields.Boolean(
        string="Active",
        default=True,
    )
    note = fields.Text(
        string="Note",
    )
